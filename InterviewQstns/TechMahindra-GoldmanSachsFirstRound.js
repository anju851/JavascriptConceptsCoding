// find each unique number's frequency
function frequencyFun() {
  let arr = [2, 6, 8, 10, 14, 50, 20, 30, 2, 14, 10];
  let map = {};

  for (let i = 0; i < arr.length; i++) {
    if (!map[arr[i]]) {
      map[arr[i]] = 1;
    } else {
      map[arr[i]]++;
    }
  }

  console.log(map);
}
